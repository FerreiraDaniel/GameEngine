package com.dferreira.commons;

/**
 * Represents a 2D Vector with their x,y,z coordinates
 */
public class Vector2f {

	/* The coordinates them selves */
	public float x, y;

	/**
	 * The constructor of the vector
	 * 
	 * @param x
	 *            coordinate
	 * @param y
	 *            coordinate
	 */
	public Vector2f(float x, float y) {
		super();
		this.x = x;
		this.y = y;
	}
}
